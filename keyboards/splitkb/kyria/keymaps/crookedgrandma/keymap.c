bool show_peter = false;

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
    case PETER:
        if (record->event.pressed) {
            // when keycode PETER is pressed
            show_peter = !show_peter;
        }
        break;
    }
    return true;
};

